package GUI;

/**
 *
 * @author Penilla
 */
public class Bodega {
    public String ID;
    public String nombre;
    public String ciudad;

    public Bodega(String ID, String nombre, String ciudad) {
        this.ID = ID;
        this.nombre = nombre;
        this.ciudad = ciudad;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }
    
    
}
