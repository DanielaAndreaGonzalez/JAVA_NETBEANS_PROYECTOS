    public String asignarllaves(){
        int llave=0;
      Enumeration <String> llaves= tabla_partido.keys();
        while(llaves.hasMoreElements()){
            llave=Integer.parseInt(llaves.nextElement());
            break;
            
        }
        return ""+(llave+1);
        
    }
    
    
    
