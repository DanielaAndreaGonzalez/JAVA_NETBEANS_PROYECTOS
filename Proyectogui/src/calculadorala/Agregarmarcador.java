    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        
      if(!txtnombrevisitante.getText().equals("")&&!txtgolesvisitantes.equals("") && txtnombrelocal.getText().equals("")&& txtgoleslocal.getText().equals(""))  
      {
      String nombrelocal=txtnombrelocal.getText();
      int goleslocal=Integer.parseInt(txtgoleslocal.getText());
      String nombrevisitante=txtnombrevisitante.getText();
      int golesvisitante=Integer.parseInt(txtgolesvisitantes.getText());
      
      
       String idpartido=asignarllaves(); 
       Partido mipartido= new Partido(nombrelocal, nombrevisitante, goleslocal, golesvisitante);
      tabla_partido.put(idpartido, mipartido);
          btnlimpiarActionPerformed(evt);
      
      JOptionPane.showMessageDialog(null, "El Marcador se ha agregado con éxito", "Exito!", JOptionPane.INFORMATION_MESSAGE);
      
      }
      else{
      JOptionPane.showMessageDialog(null, "Los Campos con * son Obligatorios", "Advertencia ", JOptionPane.WARNING_MESSAGE);    
          
      }
       
    }//GEN-LAST:event_btnguardarActionPerformed

